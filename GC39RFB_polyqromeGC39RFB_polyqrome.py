from PIL import Image
text = '''000000011111010000000
011111010111110111110
010001010111010100010
010001011010010100010
010001010100010100010
011111010010010111110
000000010101010000000
111111110001111111111
001011001101110001001
010001110111111100110
011011011100011110011
001111100101100000101
110101011101011111110
111111110010010100011
000000010001110111101
011111011111011110001
010001011110001110011
010001010101101100100
010001011001010010010
011111010010111010010
000000010100011000011'''.split('\n')

output_image = Image.new(mode="1", size=(len(text[0]), len(text)), color=255)  

for i in range(len(text)):
    for j in range(len(text[i])):
        if text[i][j] == '0':
            output_image.putpixel((j, i), 0)  # Black pixel
        elif text[i][j] == '1':
            output_image.putpixel((j, i), 255)  # White pixel

output_image.save('qrcode.jpg')
output_image.show()

